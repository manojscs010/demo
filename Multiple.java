import java.util.*;
import java.io.*;
class Multiple
{
    public static void main(String args[])
    {
      Scanner sc=new Scanner(System.in);
       int n=sc.nextInt();
       int i=0,c=0;
      for(i=1;i<=n;i++)
      {
          c=i*n;
          System.out.print(c);
      }
        
    }
}



